/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_TIM3_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_USART2_UART_Init();
  MX_TIM6_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
  
  // Encoder
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_1);
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_2);
  encoder.max = 32;
  ENC_Init(&encoder);

  // Motor
  motorInit(&htim1);
  
  // Display Init
  EEPROM_Read(moisturePlan.data);
  SSD1306_Init();  // initialise  
  SSD1306_Clear();
  SSD1306_GotoXY (5,0);
  SSD1306_Puts ("Starting Up!", &Font_7x10, SSD1306_COLOR_WHITE);
  SSD1306_GotoXY (20,35);
  SSD1306_Puts ("LOADING", &Font_11x18, SSD1306_COLOR_WHITE);
  SSD1306_UpdateScreen();
  
  // Flow Meter
  HAL_TIM_Base_Start_IT(&htim2);
  HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_3);
  
  // RTC
  rtcStart();
  rtcVbatEnable();
  
  // Test Code
  setMotorSpeed(&htim1, 0, 10000);
  HAL_Delay (500);
  SwitchScreens(MAIN);
  setMotorSpeed(&htim1, 0, 0);

  

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    
    if(ten_mS_Flag){
      ten_mS_Flag = false;
      // ********************** Fast Update Values ********************** //
      ENC_Update(&encoder);
      HAL_ADC_Start_DMA(&hadc1, &analogIn, 1);
      currMoisture = convertMoisture(analogIn);
      if(encoder.direction){
        ProcessKeyCodeInContext(BUT_MOV, &encoder);
        two_S_Flag = false;
      }
      
    }
    
    if(twentyfive_mS_Flag){
      twentyfive_mS_Flag = false;
      
        if (currMoisture < moisturePlan.data[hour[0]]){
          setMotorSpeed(&htim1, 0, 40000);
        } else {
          stopMotor(&htim1);
          hourChanged = false;
        }

    }
    
    if(hundred_mS_Flag){
      hundred_mS_Flag = false;
      
      // ********************** Button Debouncing ********************** //
      currKeyCode = getKeyCode();
      if (currKeyCode) {
        if (buttonDebounced == true) {
          if (buttonProcessed == false) { 
            buttonProcessed = ProcessKeyCodeInContext(currKeyCode, &encoder); 
          }
        }
        else {
          buttonDebounced = true;
        }
      }
      else {
        buttonDebounced = false;
        buttonProcessed = false;
      }      

      twoHuundred_mS_Switch ^= true;
      
      
      if (twoHuundred_mS_Switch == true) {
        UpdateScreenValues(currMoisture, Frequency);
      }
      
    }
    
    if(one_S_Flag){
      one_S_Flag = false;
      
      // ********************** MOTOR Debug ********************** //
      
      if (runMotor == true) {
        setMotorSpeed(&htim1, pulseBLI, pulseALI);
      } else {
        stopMotor(&htim1);
      }
      
      
      // ********************** RTC ********************** //
      if (readRTC == true) {
//        readRTC = false;
        status = rtcReadTime(rtcDataIn);
      }

      if (writeRTC == true) {
        writeRTC = false;
        status = rtcWriteTime(0,8,writeHour,2,29,6,21); // Calibrate time
      }
      
      if (startRTC == true) {
        startRTC = false;
        rtcStart();
      
      }

      // ********************** EEPROM ********************** //
      if (writeDefaultROM == true){
        writeDefaultROM = false;
        EEPROM_Write(ROM_DEFAULT);
      }
      if (readROM == true){
        readROM = false;
        EEPROM_Read(dataRead);
      }
      
      
      if (two_S_Flag) {
      // ***************** Encoder Wait Logic **************** //
        if(!encoder.direction){
          ProcessKeyCodeInContext(BUT_WAIT, &encoder);
        }
      }
      two_S_Flag ^= true;
    }
    
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

