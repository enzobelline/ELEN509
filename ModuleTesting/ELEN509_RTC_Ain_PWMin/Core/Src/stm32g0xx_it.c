/**
  * @brief This function handles TIM14 global interrupt.
  */
void TIM14_IRQHandler(void)
{
  /* USER CODE BEGIN TIM14_IRQn 0 */
  if (savePwmCapture == true) {
    if (risingEdge == true){
      firstCapture = HAL_TIM_ReadCapturedValue(&htim14, TIM_CHANNEL_1);
      risingEdge = false;
      __HAL_TIM_SET_CAPTUREPOLARITY(&htim14, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
    }
    else {
      secondCapture = HAL_TIM_ReadCapturedValue(&htim14, TIM_CHANNEL_1);
      pwmCapture = secondCapture - firstCapture;
      risingEdge = true;
      __HAL_TIM_SET_CAPTUREPOLARITY(&htim14, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
      __HAL_TIM_SET_COUNTER(&htim14, 0);
        
    }
  }
  /* USER CODE END TIM14_IRQn 0 */
  HAL_TIM_IRQHandler(&htim14);
  /* USER CODE BEGIN TIM14_IRQn 1 */

  /* USER CODE END TIM14_IRQn 1 */
}

